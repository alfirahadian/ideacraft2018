					Sebelum mengadakan Pitch First 2017, IDEACRAFT telah menyelenggarakan sebuah grand seminar pada bulan Agustus lalu. Event mendatang dari ajang bisnis dan ekonomi ini adalah Marketition 2017: National Marketing Competition yang ditujukan untuk mahasiswa Pascasarjana se-Indonesia.
