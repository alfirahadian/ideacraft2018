		          <p style="text-align: justify;">Kabar24.com, JAKARTA - Gaya hidup digital merupakan peluang bagi mahasiswa untuk memberikan solusi pemasaran sebuah produk secara tepat agar diterima pasar. <br><br>

					Magister Manajemen Sekolah Bisnis dan Ekonomi Universitas Prasetiya Mulya menawarkan tantangan kepada  para mahasiswa pasca sarjana di bidang ekonomi, bisnis, dan manajemen se Indonesia untuk turut menyelesaikan sebuah case di bidang pemasaran dalam ajang Marketition 2017. <br><br>

					"Kali ini kami menyodorkan kasus bagaimana meluncurkan sebuah kategori layanan keuangan baru yang disebut sebagai life finance ke pasar," ujar Rudy Handoko, Wakil Dekan III bidang Kemahasiswaan dan Hubungan Alumni Sekolah Bisnis Universitas Prasetiya Mulya.  <br><br>

					Dia menjelaskan, Marketition 2017 kali ini memperkenalkan Jenius sebagai produk baru life finance.<br><br>

					Ajang tahunan yang telah dilaksanakan sejak 2013 tersebut, pada tahun ini berkolaborasi dengan Jenius-PT Bank Tabungan Pensiunan Nasional Tbk (BTPN) sebagai case provider.<br><br>

					Rudi menjelaskan, Marketition yang dilaksanakan pertama kali sejak 2013, telah bekerja sama dengan beberapa pihak atau stakeholders yang turut mendukung suksesnya acara. Pada 2013 dimulai dari Triniti Optima Production (2013), Shell (2014), Sembilan Matahari (2015), dan Sequis (2016).<br><br>

					“Marketition juga hadir sebagai media baru bagi pelaku bsinis untuk menyelesaikan berbagai tantangan yang dihadapinya . Khususya di bidang pemasaran,” ujar Rudy.<br><br>

					Dia menambahkan, Marketition 2017 berada dalam naungan sebuah multievent bernama IDEACRAFT yang diselenggarakan oleh Program Pascasarjana Sekolah Bisnis dan Ekonomi Universitas Prasetiya Mulya. <br><br>

					"Ini merupakan acara ketiga (terakhir) dalam rangkaian kegiatan IDEACRAFT.  Sebelumnya, pada 11 Agustus sukses menyelenggarakan grand seminar dan pada september diadakan Pitch First 2017: National Business Pitching Competition bagi mahasiswa Sarjana se-Indonesia."<br><br>

					Acara Marketition 2017 disponsori oleh Jenius, Sunpride, Madurasa, Dua Kelinci, Vivere, dan Circledoo.id.<br><br>

					Head of Digital Banking Bank BTPN Peterjan Van Nieuwenhuizen mengatakan, perubahan baru yang begitu cepat akibat  berkembangnnya era digital. <br><br>

					"Kami yakin produk life finance dari Jenius bisa diterima oleh generasi milenial," ungkapnya. <br><br>

					Menurutnya, life finance Jenius merupakan produk layanan perbankan pertama kali di Indonesia guna memberikan solusi digital life. Nasabah, misalnya tidak harus menyimpan dana terlebih dahulu seperti halnya layanan bank konvensional. <br><br>

					"Jenius hadir sebagai wadah untuk memberikan layanan keuangan terkini, sesuai dengan permintaan. Misalnya, kami memberikan solusi bagi mereka yang hendak melancong ke Jepang,” jelasnya.<br><br>

					Marketition 2017 mengundang seluruh universitas atau institut bisnis, ekonomi dan manajemen se-Indonesia, dan pada tahun ini diikuti oleh Sekolah Bisnis dan Ekonomi - Universitas Prasetiya Mulya, PPM School of Management, Universitas Bakrie, SBM ITB, Universitas Airlangga, MM UI, Universitas Sanggabuana YPKP, UGM, Sekolah Bisnis IPB, dan Universitas Surabaya. <br><br>

					Adapun tim yang lolos ke babak semifinal pada 25 November 2017 sebanyak 16 tim, dan akan diseleksi menjadi 7 tim pada babak grand final. Pada babak grand final akan dipilih 3 tim terbaik yang akan memperebutkan total hadiah senilai Rp48,5 juta. 
