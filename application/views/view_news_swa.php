		          <span class="card-title">Marketition 2017 Menantang Mahasiswa Selesaikan Kasus Pemasaran</span><br>
		          <center><img src="https://s3-ap-southeast-1.amazonaws.com/swa.co.id/wp-content/uploads/2017/11/28181627/Marketition-2017-669x400.jpg" width="700"></center><br>
		          <p style="text-align: justify;" class="menjorok">Ketidakpastian serta kompleksitas dalam bisnis merupakan tantangan nyata yang perlu dipahami oleh setiap stakeholders, baik itu para civitas akademika maupun para pemegang keputusan di setiap perusahaan.

				Interaksi antara para pelaku bisnis dengan civitas akademika perlu dikembangkan menjadi penciptaan rekomendasi inovatif dalam bidang pemasaran yang nantinya dapat diimplementasikan pada desain bisnis perusahaan.<br><br>

				Berangkat dari pemikiran tersebut, Magister Manajemen Sekolah Bisnis dan Ekonomi Universitas Prasetiya Mulya melalui Management Society mengadakan Marketition 2017. Secara singkat, acara Marketition adalah kompetisi yang ditujukan bagi mahasiswa/i pascasarjana jurusan bisnis dan manajemen di seluruh Indonesia untuk menciptakan proses dan solusi inovatif di bidang pemasaran terkait dengan problematika yang dihadapi oleh perusahaan.<br><br>

				Marketition yang dilaksanakan pertama kali sejak 2013, telah bekerja sama dengan beberapa pihak atau stakeholders yang turut mendukung suksesnya acara ini. Dimulai dari Triniti Optima Production (2013), Shell (2014), Sembilan Matahari (2015), Sequis (2016). Tahun 2017, acara ini disponsori Jenius - PT Bank Tabungan Pensiunan Nasional Tbk (BTPN) sebagai case provider, Sunpride, Madurasa, Dua Kelinci, Vivere serta Circledoo.id.<br><br>

				Perubahan dunia yang begitu cepat dari sebelumnya, turut diakibatkan oleh berkembangnya era digital seperti saat ini. Hal ini turut dirasakan dari berbagai aspek kehidupan sehari-hari kita. Akan tetapi, begitu cepatnya perkembangan dunia digital masih belum dirasakan dalam industri layanan keuangan khususnya yang terhubung kepada aktivitas kita sehari-hari.<br><br>

				Alhasil, muncul sebuah pertanyaan: bagaimana industri layanan keuangan mampu mendukung aktivitas kita sehari-hari dalam era digital seperti saat ini? Oleh karena itulah Jenius hadir sebagai wadah untuk memberikan layanan keuangan terkini yang mampu mendukung aktivitas dan kehidupan sehari-hari. Dengan tema “Reinventing Banking, The Jenius Way”, Marketition 2017 berkolaborasi dengan Jenius untuk mengundang dan menantang para mahasiswa pascasarjana bidang ekonomi, bisnis, dan manajemen se-Indonesia untuk turut menyelesaikan sebuah case dibidang pemasaran.<br><br>

				Kasus yang diberikan adalah bagaimana meluncurkan sebuah kategori layanan keuangan baru yang disebut sebagai “Life Finance” ke pasar dan memperkenalkan Jenius sebagai produk baru dalam kategori tersebut.<br><br>

				Dr Rudy Handoko, Juri Marketition 2017 yang juga Wakil Dekan III Bidang Kemahasiswaan dan Hubungan Alumni Universitas Prasetiya Mulya, mengatakan, ada beberapa aspek yang dinilai dalam kompetisi ini. Aspek-aspek yang dinilai mencakup cara presentasi, solusi yang ditawarkan, seberapa aplikatif solusi yang ditawarkan, dan prospek revenue/profit dari solusi yang ditawarkan. Menurutnya, melalui kompetisi ini diharapkan akan lahir entreprenership dan intrapreneurship. Intrapreneurship merupakan sikap kewirausahaan karyawan di dalam perusahaan.<br><br>

				Marketition 2017 berada dalam naungan sebuah multi-event bernama IDEACRAFT yang diselenggarakan oleh Program Pascasarjana Sekolah Bisnis dan Ekonomi - Universitas Prasetiya Mulya bersama para mahasiswa pascasarjanya.<br><br>

				Marketition 2017 merupakan acara ketiga (terakhir) dalam serangkaian kegiatan Ideacraft 2017. Dimana pada 11 Agustus 2017 yang lalu telah sukses menyelenggarakan grand seminar dan September 2017 telah mengadakan Pitch First 2017: National Business Pitching Competition bagi mahasiswa S1 se-Indonesia.<br><br>

				Marketition 2017 juga mengundang seluruh universitas atau institut bisnis, ekonomi dan manajemen se-Indonesia, dan tahun ini diikuti oleh Sekolah Bisnis dan Ekonomi – Universitas Prasetiya Mulya, PPM School of Management, Universitas Bakrie, SBM ITB, Universitas Airlangga, MM UI, Universitas Sanggabuana YPKP, UGM, Sekolah Bisnis IPB, dan Universitas Surabaya. Adapun tim yang lolos ke babak semifinal pada 25 November 2017 sebanyak 16 tim, dan akan diseleksi menjadi 7 tim pada babak grand final. Pada babak grand final akan dipilih 3 tim terbaik yang akan memperebutkan total hadiah Rp 48,5 juta.<br><br>

				Marketition 2017 menghadirkan 5 juri yang terdiri dari para akademisi dan praktisi yang berpengalaman, yaitu: Dr. Rudy Handoko, Wakil Dekan III Bidang Kemahasiswaan dan Hubungan Alumni, di Sekolah Bisnis dan Ekonomi - Universitas Prasetiya Mulya. Lalu, Peterjan Van Nieuwenhuizen, Head of Digital Banking di PT BTPN. Berikutnnya, Michael Hartawan Koentjoro, Strategic Communication and Creative Chapter Lead di Jenius. Juga, Cecilia Hermanto, Manager and Head of Financial Service Industry di MarkPlus Inc. dan Ruben Saragih, konsultan di Prasetiya Mulya Executive Learning Institute.<br><br>

				Melalui kompetisi ini terdapat tujuan yang ingin dicapai. Marketition ingin menjadi sebuah platform terbaik agar para peserta dapat berkompetisi dan belajar dari satu sama lain dan membangun jaringan yang kuat di antara mereka. Tujuan lain, melatih kemampuannya dalam menyelesaikan problematika bidang pemasaran. Marketition juga hadir sebagai media bagi para pelaku bisnis untuk menyelesaikan berbagai tantangan yang dihadapinya khususnya pada bidang pemasaran.
