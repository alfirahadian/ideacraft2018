		          <center><img src="https://marketing.co.id/wp-content/uploads/2017/11/prasetia_mulia.jpg" width="700"></center><br>
		          <p style="text-align: justify;">Jakarta - Universitas Prasetiya Mulya melalui Management Society kembali menggelar Marketition 2017. Marketition merupakan kompetisi yang diperuntukkan bagi mahasiswa dan mahasiswi pascasarjana jurusan bisnis dan manajemen di seluruh Indonesia. Melalui kompetisi ini para peserta ditantang untuk menciptakan proses dan solusi inovatif di bidang pemasaran terkait problematika yang dihadapi perusahaan.<br><br>
			
		          	Marketition pertama kali digelar di tahun 2013. Setiap tahun penyelenggarannya Marketition bekerja sama dengan perusahaan yang berbeda. Dimulai dengan Triniti Optima Production (2013), Shell (2014), Sembilan Matahari (2015), dan Sequis (2016). Tahun ini Marketition berhasil menggandeng Jenius-Bank Tabungan Pensiunan Nasional. Sebagai sponsor Jenius berhak menjadi case provider.<br><br>

			

					Dengan tema “Reinventing Bank, The Jenius Way”, para mahasiswa di bidang bisnis, ekonomi, dan manajemen bersaing untuk menyelesaikan sebuah case study di bidang pemasaran. Adapun studi kasus yang harus dibahas bagaimana meluncurkan kategori layanan keuangan baru yang disebut sebagai Life Finance ke pasar dan memperkenalkan Jenius sebagai produk baru dalam kategori tersebut.<br><br>

			

					Dr Rudy Handoko, Juri Marketition 2017 yang juga Wakil Dekan III Bidang Kemahasiswaan dan Hubungan Alumni Universitas Prasetiya Mulya mengatakan, ada beberapa aspek yang dinilai dalam kompetisi ini. Aspek-aspek yang dinilai mencakup cara presentasi, solusi yang ditawarkan, seberapa aplikatif solusi yang ditawarkan, dan prospek revenue/profit dari solusi yang ditawarkan.<br><br>

				

					“Melalui kompetisi ini diharapkan akan lahir entreprenership dan intrapreneurship. Intrapreneurship merupakan sikap kewirausahaan karyawan di dalam perusahaan,” tutur Rudy.<br><br>

		
					Peterjan Van Nieuwenhuizen, Head of Digital Banking BTPN dan juri Marketition 2017 mengatakan, konsep Life Finance berbeda dengan konsep layanan perbankan konvensional yang biasanya berfokus pada produk tabungan dan kredit. “Life Finance dirancang untuk mendukung semua kebutuhan solusi keuangan nasabah. Jenius merupakan digital banking pertama di Indonesia,” tutur Peterjan.<br><br>


					Tahun ini, Marketition diikuti oleh peserta dari Universitas Prasetiya Mulya, PPM School of Management, Universitas Bakrie, SBM ITB, Universitas Airlangga, MMUI, Universitas Sanggabuana YPKP, UGM, Sekolah Bisnis IPB, dan Universitas Surabaya. Babak grand final digelar di Universitas Prasetiya Mulya dan diikuti oleh 3 tim terbaik. Total hadiah yang diberikan dalam Marketition 2017 mencapai Rp48.500.000.
